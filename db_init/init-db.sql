-- Creación de la tabla de usuarios
CREATE TABLE users (
    user_id SERIAL PRIMARY KEY,
    username VARCHAR(50) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    status VARCHAR(20) DEFAULT 'active'
);

-- Tabla de direcciones de usuario
CREATE TABLE user_addresses (
    address_id SERIAL PRIMARY KEY,
    user_id INT REFERENCES users(user_id) ON DELETE CASCADE,
    address VARCHAR(200),
    city VARCHAR(50),
    postal_code VARCHAR(10),
    country VARCHAR(50),
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabla de categorías de productos
CREATE TABLE categories (
    category_id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT
);

-- Tabla de proveedores
CREATE TABLE suppliers (
    supplier_id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    contact_email VARCHAR(100)
);

-- Tabla de productos
CREATE TABLE products (
    product_id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    description TEXT,
    price DECIMAL(10, 2) NOT NULL,
    stock INT NOT NULL CHECK(stock >= 0),
    category_id INT REFERENCES categories(category_id) ON DELETE SET NULL,
    supplier_id INT REFERENCES suppliers(supplier_id) ON DELETE SET NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabla de descuentos y promociones
CREATE TABLE discounts (
    discount_id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    discount_percent DECIMAL(5, 2) NOT NULL CHECK(discount_percent BETWEEN 0 AND 100),
    start_date DATE,
    end_date DATE
);

-- Tabla de productos y sus descuentos aplicables
CREATE TABLE product_discounts (
    product_discount_id SERIAL PRIMARY KEY,
    product_id INT REFERENCES products(product_id) ON DELETE CASCADE,
    discount_id INT REFERENCES discounts(discount_id) ON DELETE CASCADE
);

-- Tabla de pedidos
CREATE TABLE orders (
    order_id SERIAL PRIMARY KEY,
    user_id INT REFERENCES users(user_id) ON DELETE SET NULL,
    status VARCHAR(20) DEFAULT 'pending',
    order_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    shipping_address_id INT REFERENCES user_addresses(address_id) ON DELETE SET NULL,
    total_amount DECIMAL(10, 2) CHECK(total_amount >= 0)
);

-- Tabla de estados de los pedidos (tracking)
CREATE TABLE order_status (
    status_id SERIAL PRIMARY KEY,
    order_id INT REFERENCES orders(order_id) ON DELETE CASCADE,
    status VARCHAR(20),
    status_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Tabla de artículos de pedidos
CREATE TABLE order_items (
    order_item_id SERIAL PRIMARY KEY,
    order_id INT REFERENCES orders(order_id) ON DELETE CASCADE,
    product_id INT REFERENCES products(product_id) ON DELETE SET NULL,
    quantity INT NOT NULL CHECK(quantity > 0),
    price DECIMAL(10, 2) NOT NULL CHECK(price >= 0)
);

-- Trigger para actualizar el stock en la tabla de productos cuando se inserta un artículo en un pedido
CREATE OR REPLACE FUNCTION trg_update_stock() RETURNS TRIGGER AS $$
BEGIN
    IF NEW.quantity > (SELECT stock FROM products WHERE product_id = NEW.product_id) THEN
        RAISE EXCEPTION 'Not enough stock for product %', NEW.product_id;
    END IF;
    UPDATE products SET stock = stock - NEW.quantity WHERE product_id = NEW.product_id;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER trg_order_items_insert
AFTER INSERT ON order_items
FOR EACH ROW EXECUTE FUNCTION trg_update_stock();

